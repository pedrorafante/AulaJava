public class Exercicio7 {
    /*
    Escreva um algoritmo que exiba 20 vezes a mensagem “Eu gosto de estudar Algoritmos!”.
     */
    public static void main(String[] args) {
        for (int i = 0; i < 20; i++) {
            System.out.println(i + 1 + " - Eu gosto de estudar Algoritmos!");
        }
    }
}
